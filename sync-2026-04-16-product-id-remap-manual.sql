SELECT 'tb1_produto_reserved_range' AS check_name, COUNT(*) AS total
FROM tb1_produto
WHERE tb1_id BETWEEN 3000 AND 3100

UNION ALL

SELECT 'tb1_produto_above_9999' AS check_name, COUNT(*) AS total
FROM tb1_produto
WHERE tb1_id > 9999

UNION ALL

SELECT 'tb3_vendas_invalid_refs' AS check_name, COUNT(*) AS total
FROM tb3_vendas sales
LEFT JOIN tb1_produto products ON products.tb1_id = sales.tb1_id
WHERE products.tb1_id IS NULL

UNION ALL

SELECT 'product_discards_invalid_refs' AS check_name, COUNT(*) AS total
FROM product_discards discards
LEFT JOIN tb1_produto products ON products.tb1_id = discards.product_id
WHERE products.tb1_id IS NULL

UNION ALL

SELECT 'tb25_produto_movimentacoes_invalid_refs' AS check_name, COUNT(*) AS total
FROM tb25_produto_movimentacoes stock
LEFT JOIN tb1_produto products ON products.tb1_id = stock.product_id
WHERE products.tb1_id IS NULL;
